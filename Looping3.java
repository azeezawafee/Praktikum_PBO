package Tugas6;

//Huruf Z-A
public class Looping3 {
    public static void main(String[]args){
        System.out.println("c. Huruf Z-A");
        for(char c = 'Z'; c >= 'A'; c--) {
            System.out.print(c + " ");
        }
        System.out.println();
    }
}
